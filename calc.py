def add(num1,num2):
    sum=num1+num2
    return sum
def sub(num1,num2):
    sub=num1-num2
    return sub
def div(num1,num2):
    div=num1/num2
    return div
def muiltiply(num1,num2):
    muiltiply=num1*num2
    return muiltiply
def power(num1,num2):
    power=num1**num2
    return power
def remainder(num1,num2):
    remainder=num1%num2
    return remainder
